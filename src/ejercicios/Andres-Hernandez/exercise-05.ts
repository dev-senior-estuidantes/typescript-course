/* 5. Calcular el área de un rectángulo
•	Explicación: Se multiplica la base por la altura.
•	Objetivo: Aplicar operaciones matemáticas básicas.
•	Pregunta: ¿Cómo modificarías el código para que también calcule el perímetro?
 */

let base: number = 10;
let altura: number = 10;
let area: number = base * altura;
console.log(`El area del rectangulo es: ${area}`);

/* Respuesta pregunta
El perimetro se calcula multiplicando la base y la altura por 2 de la siguiente manera:
*/

let base2: number = 10;
let altura2: number = 10;
let perimetro2: number = (base2 + altura2) * 2;
console.log(`El perimetro del rectangulo es: ${perimetro2}`);