// Uso de template literals

// Declaramos algunas variables
let nombre3: string = "V";
let edad3: number = 25;

// Usamos template strings (backticks `) para insertar variables fácilmente
let mensaje3: string = `Hola, mi nombre es ${nombre3} y tengo ${edad3} años.`;

// Mostramos el mensaje
console.log(mensaje3);

